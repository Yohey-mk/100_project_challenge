# CLI Ver.
#1. 日付、品目、金額を1件ずつ入力（辞書で管理）
#2. 入力した支出をリストに追加
#3. 現在のリスト表示＆合計金額を出力
#4. 「続ける or 終了」を選べる
# GUI Ver.
#	•	Fletで以下を実装：
